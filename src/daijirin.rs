fn daijirin_body_converter(body: &String) -> Option<Vec<String>>
{
    let body = daijirin_gaiji_fixer(body.as_str());

    let mut entries: Vec<String> = body.trim().split('\n').map(|s| s.to_string()).collect();
    let definition = entries.drain(..).map(|mut s| s.drain(..).collect()).collect();

    Some(definition)
}

fn daijirin_heading_converter(heading: &String, waei_and_eiwa: bool) -> Option<(String, Vec<String>)>
{
    let heading = daijirin_gaiji_fixer(heading.as_str());
    if !waei_and_eiwa
    {
        if heading.ends_with("（和英）") { return None; }
        if heading.ends_with("（英和）") { return None; }
    }

    let mut reading = vec!();
    let mut spellings = vec!();
    let heading_chars = heading.chars().collect::<Vec<char>>();
    let mut i = 0;
    let mut mode = 0; // 0 : reading; 1 : spelling; 2 : after spelling
    while i < heading_chars.len()
    {
        match mode
        {
            0 =>
                {
                    match heading_chars[i]
                    {
                        // unmarked eiwa? entries e.g. "arc", "argument"
                        '〖' if waei_and_eiwa => {
                            spellings.push(vec!());
                            mode = 1;
                        }
                        '〖' if !waei_and_eiwa => break,
                        '〗' if waei_and_eiwa => panic!("unknown state"),
                        '[' | '】' => panic!("unknown state"),
                        '【' =>
                            {
                                spellings.push(vec!());
                                mode = 1;
                            }
                        '-' | '・' => (),
                        c => reading.push(c)
                    }
                }
            1 =>
                {
                    match heading_chars[i]
                    {
                        '】' | '〗' => mode = 2,
                        '[' | '【' | '〖' => panic!("unknown state"),
                        '(' | ')' => (),
                        '・' =>
                            {
                                spellings.push(vec!());
                            }
                        c => spellings.last_mut().unwrap().push(c),
                    }
                }
            2 =>
                {
                    match heading_chars[i]
                    {
                        '（' => break,
                        _ => panic!("unknown state"),
                    }
                }
            _ => panic!("unknown state")
        }
        i += 1;
    }
    let reading = reading.drain(..).collect();
    let mut spellings: Vec<String> = spellings.drain(..).map(|mut s| s.drain(..).collect()).collect();
    if spellings.is_empty()
    {
        spellings.push("".to_string());
    }

    Some((reading, spellings))
}

fn daijirin_gaiji_fixer(text: &str) -> String
{
    let mut text = text.to_string();

    for mapping in GAIJI_MAPPING.iter()
    {
        text = text.replace(mapping.0, mapping.1);
    }
    text
}

